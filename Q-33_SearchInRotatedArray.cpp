class Solution {
public:
    int search(vector<int>& nums, int target) {
        int n=nums.size();
        int i=0;
        int ans=-1;
        
        while(i<n){
            
            if(nums[i]==target)
            {
                ans=i;
                break;
            }
            else{
                i++;
            }
            
        }
        
    return ans;}
};